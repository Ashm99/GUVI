package question4;

public class Person { 
	private String name;
	private int age;
	
	//getter and setter methods for the attributes
	public void setName(String name) {
		this.name = name;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return this.name;
	}
	public int getAge() {
		return this.age;
	}
}
